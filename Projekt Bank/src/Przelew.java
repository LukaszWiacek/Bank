    public Przelew(String[] rachunki,String pes) throws ClassNotFoundException, SQLException {
        initComponents();
        rachu=new String[rachunki.length];
        rachu=rachunki;
        jPrzeZR.removeAllItems();
        
        pesel=pes;
        
        Polaczenie conn=new Polaczenie();
        ResultSet result = conn.stmt.executeQuery("select nrrachunku from rachunek");
        result.last();
        int count=result.getRow();
        nar=new String[count];
        for(int j=0;j<count;j++)
        {
            result.absolute(j+1);
            nar[j]=result.getObject("nrrachunku").toString();
        }
        for(int i=0;i<rachu.length;i++){
            result = conn.stmt.executeQuery("select nrrachunku from rachunek where idrachunku="+rachu[i]+"");
            result.last();
            jPrzeZR.addItem(result.getObject("nrrachunku").toString());
        }
        jPrzeNaR.removeAllItems();
        conn.con.close();
    }

